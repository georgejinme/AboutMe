import Foundation
import UIKit


extension UIColor{
    class func DarkSlateGrayLight() -> UIColor {
        return UIColor(red: 151 / 255, green: 1, blue: 1, alpha: 1)
    }
    class func DarkSlateGray() -> UIColor {
        return UIColor(red: 141 / 255, green: 238 / 255, blue: 238 / 255, alpha: 1)
    }
    class func DarkSlateGrayDeep() -> UIColor {
        return UIColor(red: 121 / 255, green: 205 / 255, blue: 205 / 255, alpha: 1)
    }
    class func DarkSlateGrayMoreDeep() -> UIColor{
        return UIColor(red: 82 / 255, green: 139 / 255, blue: 139 / 255, alpha: 1)
    }
}